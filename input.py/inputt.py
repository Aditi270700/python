# Getting user Input and Type Casting

# input()
# int()
# float()
# eval()

# input()
# input takes value as a string
a=(input("Enter my value1 :- "))
b=(input("Enter my value2 :- "))
print(a,b)


# int()
# int is used take integer value
c=int(input("enter value1:-"))
d=int(input("enter value2:-"))
print(c+d)

# float()
# float is used to decimal value return
e=float(input("enter value1:-"))
f=float(input("enter value2:-"))
print(e+f)

# eval()
# eval is used to convert binary to integer
g=eval(input("Enter the value1:- "))
h=eval(input("Enter the value2:- "))
print(g+h)
