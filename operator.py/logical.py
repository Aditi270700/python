# logical operator
# it is used to compare the value like used and,or,not operator

# and operator is used to returns true if both statements are true
# or operator is used to returns true if one statements and both statements are true
# not operator is used to returns false ,if the results is true

# And operator
x=10
y=20
print(x==10 and x<y)

# or operator

print(x==10 or x>10)
print(x>y or x==y)

# not operator
print(x!=20)
print(x!=10)